export function DictClubs(clubName){
    if(clubName === "Manchester Ci") return "Manchester City"
    else if(clubName === "Manchester Un") return "Manchester United"
    else if(clubName === "Crystal Palac") return "Crystal Palace"
    else if(clubName === "Sheffield Uni") return "Sheffield United"
    else if(clubName === "Leeds Utd") return "Leeds United"
    else return clubName
}
export default DictClubs;
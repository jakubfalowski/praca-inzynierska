export function dictClubs(clubName){
    if(clubName === "Warta") return "Warta Poznan"
    else if(clubName === "Korona") return "Korona Kielce"
    else if(clubName === "Gornik Z.") return "Gornik Zabrze"
    else if(clubName === "Legnica") return "Miedz Legnica"
    else if(clubName === "Lech") return "Lech Poznan"
    else if(clubName === "Zaglebie") return "Zaglebie Lubin"
    else if(clubName === "Rakow") return "Rakow Czestochowa"
    else if(clubName === "Lechia") return "Lechia Gdansk"
    else if(clubName === "Piast") return "Piast Gliwice"
    else if(clubName === "Slask") return "Slask Wroclaw"
    else if(clubName === "Legia") return "Legia Warszawa"
    else return clubName
}